def build_monster_prompt(monster_id: str, trigger_word: str = "jrpg_pixel_style") -> str:
    # Check NPC prompts first, then monster prompts
    if monster_id in NPC_PROMPTS:
        info = NPC_PROMPTS[monster_id]
        return (
            f"pixel art RPG NPC character sprite, {info['desc']}, "
            f"side view, {trigger_word}, 16-bit SNES style, {info['palette_hint']}, "
            f"black outline, white background, isolated character, NOT a warrior, NOT armored"
        )
    info = MONSTER_PROMPTS.get(monster_id, {"desc": monster_id.replace("_", " "), "palette_hint": "muted fantasy colors"})
    return (
        f"pixel art RPG battle monster sprite, {info['desc']}, "
        f"side view, {trigger_word}, 16-bit SNES style, {info['palette_hint']}, "
        f"black outline, white background, isolated creature"
    )


